public class Books {

    public String Title;
    public String Price;
    public String Publisher;
    public String Condition;
    public String UID;


    public Books() {

    }

    public Books(String title, String price, String publisher, String condition, String uid) {
        Title = title;
        Price = price;
        Publisher = publisher;
        Condition = condition;
        UID = uid;
    }
}

